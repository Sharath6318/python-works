# Logic_operator

a, b, c = 10, 5, 15

print(a > b and b < c)

print(c > a or b > a)

print(not(b > c))

# identity operator

x = [1, 2, 3]

y = [1, 2, 3]

z = x

print(x is z)
print(x is y)


# Membership operator

fruits = ["apple", "mango", "cherry"]

print("cherry" in fruits)
print("grape" not in fruits)


